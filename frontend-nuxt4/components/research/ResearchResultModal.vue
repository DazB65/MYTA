            >
              <svg class="h-5 w-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13 10V3L4 14h7v7l9-11h-7z"/>
              </svg>
              <span>Save as Goal</span>
            </button>
            
            <div v-if="showGoalForm" class="mt-4 space-y-3">
              <input 
                v-model="goalTitle"
                placeholder="Goal title..."
                class="w-full bg-forest-700 border border-forest-500 rounded-lg px-3 py-2 text-white placeholder-gray-400 focus:outline-none focus:border-blue-500"
              />
