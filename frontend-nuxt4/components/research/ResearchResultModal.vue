            <p class="text-gray-400 text-sm">Generated by Research Agent</p>
          </div>
        </div>
        <button @click="$emit('close')" class="text-gray-400 hover:text-white transition-colors">
          <svg class="h-6 w-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"/>
          </svg>
        </button>
      </div>

      <!-- Modal Content -->
      <div class="flex h-[calc(90vh-140px)]">
        <!-- Research Content -->
        <div class="flex-1 p-6 overflow-y-auto">
          <div class="prose prose-invert max-w-none">
            <div class="whitespace-pre-wrap text-gray-200 leading-relaxed">{{ note?.content }}</div>
          </div>
        </div>

        <!-- Action Panel -->
        <div class="w-80 bg-forest-800 border-l border-forest-600 p-6 overflow-y-auto">
          <h3 class="text-lg font-semibold text-white mb-4">Save Options</h3>
          
          <!-- Save as Task -->
          <div class="mb-6">
            <button 
              @click="showTaskForm = !showTaskForm"
              class="w-full bg-blue-600 hover:bg-blue-700 text-white px-4 py-3 rounded-lg transition-colors flex items-center justify-center space-x-2"
            >
              <svg class="h-5 w-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 5H7a2 2 0 00-2 2v10a2 2 0 002 2h8a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2"/>
