<script setup lang="ts">
import { ref, watch } from 'vue'

interface Props {
  show: boolean
  note: any
}

interface Emits {
  (e: 'close'): void
  (e: 'saveAsTask', data: any): void
  (e: 'saveAsGoal', data: any): void
}

const props = defineProps<Props>()
const emit = defineEmits<Emits>()

// Form states
const showTaskForm = ref(false)
const showGoalForm = ref(false)

// Task form data
const taskTitle = ref('')
const taskDescription = ref('')
const taskPriority = ref('medium')

// Goal form data
const goalTitle = ref('')
const goalDescription = ref('')
const goalTarget = ref('')

// Watch for note changes to populate forms
watch(() => props.note, (newNote) => {
  if (newNote) {
    taskTitle.value = `Research: ${newNote.title}`
    taskDescription.value = newNote.content
    goalTitle.value = `Goal: ${newNote.title}`
    goalDescription.value = newNote.content
  }
}, { immediate: true })

// Methods
const saveAsTask = () => {
  const taskData = {
    title: taskTitle.value,
    description: taskDescription.value,
    priority: taskPriority.value,
    category: 'research',
    tags: ['research', 'agent-generated'],
    dueDate: new Date(Date.now() + 7 * 24 * 60 * 60 * 1000) // 1 week from now
  }
  
  emit('saveAsTask', taskData)
  showTaskForm.value = false
}

const saveAsGoal = () => {
  const goalData = {
    title: goalTitle.value,
    description: goalDescription.value,
    target: goalTarget.value,
    category: 'research',
    tags: ['research', 'agent-generated']
  }
  
  emit('saveAsGoal', goalData)
  showGoalForm.value = false
}

const copyToClipboard = async () => {
  try {
    await navigator.clipboard.writeText(props.note?.content || '')
    // You could add a toast notification here
  } catch (err) {
    console.error('Failed to copy to clipboard:', err)
  }
}

const exportToPDF = () => {
  // PDF export functionality would go here
  console.log('Exporting to PDF...')
}
